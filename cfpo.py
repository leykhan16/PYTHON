#Arithmetic Operators
#+	Addition
bill = 100
tip = 20
total = bill + tip
print(total)
#-	Subtraction
difference = bill - tip
print(difference)
#*	Multiplication
product = bill * tip
print(product)
#/	Division
quotient = bill / tip
print(quotient)
#%	Modulus
remainder = bill % tip
print(remainder)
#**	Exponentiation
power = bill ** tip
print(power)
#//	Floor division
floor_division = bill // tip
print(floor_division)
#modulus division
modulus_division = bill % tip
print(modulus_division)
#modulus remainder
modulus_remainder = bill % tip
print(modulus_remainder)
#modulo operator
modulo_result = bill % tip
print(modulo_result)